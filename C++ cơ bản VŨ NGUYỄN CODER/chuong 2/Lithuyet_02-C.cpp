/*
Vocabulary:
- Primitive: Kiểu nguyên thủy
- Derived: Kiểu mở rộng
- character: Kiểu kí tự


** Các kiểu kí tự nguyên thủy (Primitive):
- int: Số nguyên (4 byte)
- float/double: số thực (float: 4 byte, double: 8 byte)
- char: kí tự (1 byte)
- bool: đúng/sai (1 byte) (chỉ có trong C++ / muốn dùng trong C phải khai báo thư viện #include <stdbool.h>)
.......

** Các kiểu dữ liệu mở rộng (Derived): 
- Pointer: Con trỏ
- Reference (C++): 
- Function: Hàm (hay còn được gọi là chương trình con)
- Array, Struct, class
- STL: std::string (Chuỗi kí tự, khi dùng cần khai báo thư viện string) 
........  
*/