/*
**Vocabulary:
- square root: Căn bậc hai
- arithmetic operator: Phép toán số học
- Argument: Đối số
- Fixed value = Literal: Giá trị cố định
- Expression: Biểu thức
- Precedence: Độ ưu tiên
- add: Cộng
- Concatenate: Nối
***
- Phép toán (Operation) là 1 ký hiệu/câu lệnh cho phép tính toán gì đó dựa trên giá trị của các biến để tính ra được 1 kết quả nào đó.
 Ví dụ: Dấu + là phép cộng 2 số
- Trong 1 phép toán cần có sự tham gia của các biến, hoặc giá trị, và các thành phần này được gọi chung là toán hạng (Operand). 
 Ví dụ: Phép cộng cần có 2 toán hạng tham gia
Sự kết hợp giữa toán tử và toán hạng được gọi là biểu thức (expression). Ví dụ: x + y là 1 biểu thức gồm toán tử + và 2 toán hạng x,y
- Một phép toán có 5 đặc điểm cần lưu ý:
1. Cú pháp (syntax):
    - toán tử (Operator): kí hiệu, kí tự -> toán hạng
    - hàm (function): kí tự (đối số)
2. Số toán hạng (operand) tham gia (ngôi)
3. Độ ưu tiên (precedence)
4. Thứ tự thực hiện (associativity)
5. Giá trị / kết quả trả về (return value)
*/


